package day03_EscapeSequences;
/*
Escape sequences: MUST be given with double quote
     \n: starts a new line
     \t: paragraph space (tab)
     \\: single back slash
     \": double quote


 */

public class EscapeSequences {

    public static void main(String[] args){
        System.out.println("Java \nPython \nC#");

        System.out.println("----------------------------------------------------------");

        System.out.println("Hello Cydeo \nMy name is Esra \nI am from France \nI look forward to creating a code");

        System.out.println("----------------------------------------------------------");

        System.out.println("\tJava is cool Programming Language");

        System.out.println("-----------------------------------------------------------");

        System.out.println("/ \\"); //in order to print one back slash we need to give two backward slashes

        System.out.println("-----------------------------------------------------------");

        System.out.println("My favorite TV show is \"Crown\" ");



    }


}
