package day19_LoopPractices;

public class LoopsWithoutCurlyBraces {

    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {     // Curly brace is not mandatory when inside the loop, there is just one print statement
            System.out.println("Cydeo");
        }                                 // Curly brace is not mandatory when inside the loop, there is just one print statement


        for (int i = 0; i < 5; i++)    // curly braces is not mandatory but recommended

            System.out.println("Batch 28");

    }
}
