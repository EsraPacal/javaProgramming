package week03;

public class UnaryReview {
    public static void main(String[] args) {


        int a = 20;
        a++;

        System.out.println("a = " + a);

        int b= 20;
        System.out.println("b = " + ++b);

        int c = 20;

        System.out.println(++c + 1);   // 22




    }
}
